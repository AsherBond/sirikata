#ifndef SCHEDULABLE_HPP_
#define SCHEDULABLE_HPP_
namespace Meru {
class Scheduler;
class Schedulable {
public:

    virtual ~Schedulable() {}

    /**
     * Says number of schedulable jobs at that moment.
     * May use the opportunity to coalesce results from other threads
     * into schedulable work queue.  Should only be called by the scheduler
     * will be called by the scheduler at least once a frame
     */
    virtual unsigned int numSchedulableJobs()=0;
    virtual bool processOneJob()=0;
};
}
#endif
